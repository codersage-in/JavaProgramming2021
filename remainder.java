import java.util.*;
import java.lang.*;
import java.io.*;
class Codechef {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		int t =sc.nextInt();
		int a;
		int b;
		for(int i=0;i<t;i++) {
			a=sc.nextInt();
			b=sc.nextInt();
			int r=a%b;
			System.out.println(r);
			r=0;
		}
		

	}

}
