package JavaProgramming2021;

import java.util.Scanner;

public class Fake_GCD {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int T = sc.nextInt();
        while(T-- > 0){
            int N = sc.nextInt();
            for(int i=1;i<=N;i++){
                System.out.println(i);
            }
        }
    }
}
